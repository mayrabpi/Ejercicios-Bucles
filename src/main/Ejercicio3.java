package main;

public class Ejercicio3 {

	public static void main(String[] args) {
		// Mostrar tabla de multiplicar del 1 al 10
		
		int n;
		
		 for (int i = 1; i <= 10; i++) {
		        for (int j = 1; j <= 10; j++) {
		            n = i * j;
		            System.out.print(n + " ");           
		        }
		        System.out.println();
				
		 }

	}

}
